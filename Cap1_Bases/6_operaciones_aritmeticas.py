#Operadores aritmeticos para operaciones matematicas.

n1 = 2
n2 = 3

res1 = n1 + n2              #suma
res2 = n2 - n1              #resta
res3 = n1 - n2              #resta con resultado negativo
res4 = n1 * n2              #multiplicacion
res5 = n1 / n2              #division
res6 = n1 // n2             #division redondeada a la baja
res7 = n1 % n2              #modulo de la division
res8 = n1 ** n2             #potencia

print (res1,'\n',res2,'\n',res3,'\n',res4,'\n',res5,'\n',res6,'\n',res7,'\n',res8)