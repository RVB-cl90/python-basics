#operadores de asignacion
#principal uso, para ahorrar codigo

a = 0
a += 5      #suma en asignacion

print (a)

a -= 2      #resta en asignacion

print (a)

a *= 3      #multiplicacion en asignacion

print (a)

a /= 3      #division en asignacion

print (a)

a **= 2      #potencia en asignacion

print (a)

a %= 2      #modulo en asignacion

print (a)

#Para usar los operadores en asignacion, primeramente se debe declarar la variable y asignarle un valor
