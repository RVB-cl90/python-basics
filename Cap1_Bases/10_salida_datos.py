#Salidas

nombre = 'Rafael'
edad = 29

print ("Hola",nombre,", tienes",edad," años.")

'''Se puede hacer lo mismo con el comando "format".
    donde se ingresan llaves en el texto ({}), y al final de la cadena se ingresa format y las variables que se quieren ingresar en el texto.'''
print ("Hola {}, tienes {} años.".format(nombre,edad))

#el siguiente es otra forma de format que se esta usando recientemente.
print (f"Hola {nombre}, tienes {edad} años.")