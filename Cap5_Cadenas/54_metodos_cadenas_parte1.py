# Metodos para cadenas

cadena = "hola mundo".upper()

print(cadena.capitalize())

# despues de una cadena, al concatenar un punto(.), apareceran metodos comunes para cadenas

#   .tittle() para dejar en mayuscula las primeras letras de las palabras
#   .count() para contar lo especificado en su interior, pueden ser letras o palabras
#   .find() para encintrar una palabra o caracter, devuelve la posicion en la cadena donde se produzca la primera coincidencia
#   .rfind() para encontrar la ultima coincidencia
#   .isdigit() para validar si la cadena tiene solo caracteres numericos
#   .isalpha() para validar si la cadena tiene solo caracteres de letras (sin simbolos especiales)
#   .isalnum() para validar si la cadena tiene caracteres alfanumericos solamente
#   .islower() para validar si la cadena tiene solo caracteres en minuscula
#   .isupper() para validar si la cadena tiene solo caracteres en mayuscula
#   .istittle() valida si la primera letra de cada palabra es mayuscula
#   .isspace() valida si la cadena solo contiene espacios
