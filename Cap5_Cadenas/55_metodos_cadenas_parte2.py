# Metodos de cadenas

cadena = "hola mundo"

print(cadena)

#   .starswith() valida si el string comienza con lo especificado en el metodo
#   .endswith() valida si el string termina con lo especificado
#   .split() sirve para separar los elementos de la cadena formando una lista, por defecto, considera los espacios
#   .join() separa los elementos de una cadena por un caracter especificado, el metodo recive la cadena y se usa con el caracter separador
#   .strip() elimina lo especificado de la cadena, por defecto, elimina los espacios
#   .replace() reemplaza lo especificado de la cadena por lo que se adjunta