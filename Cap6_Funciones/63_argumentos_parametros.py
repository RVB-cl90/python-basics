#   Argumentos y parametros

def restar(num1,num2):      #parametros, es el nombre que se le da a las variables cuando se define una funcion
    return num1-num2

print(restar(4,3))          #argumentos, es el valor que se pasa al momento de invocar la funcion
print(restar(num2=4,num1=2)) #los argumentos se pueden pasar por nombre, especificando el nombre del paramentro relacionado

