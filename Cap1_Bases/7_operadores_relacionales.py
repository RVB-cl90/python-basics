#Operadores relacionales para evaluar variables o valores

resultado = 10 < 20         #menor que
resultado2 = 10 > 20        #mayor que
resultado3 = 10 == 20       #igual a
resultado4 = 10 != 20       #diferente a
resultado5 = 10 <= 20        #menor o igual que
resultado6 = 10 >= 20        #mayor o igual que

print(resultado)
print(resultado2)
print(resultado3)
print(resultado4)
print(resultado5)
print(resultado6)

#los operadores relacionales se pueden combinar con los aritmeticos

